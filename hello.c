int map(char level, char x, char y) {
    
    // Right now just one map
    // return game_map[40*y+x];
    return 40*y+x;
}


void draw_screen() {

    int row,col;
    for(row=0; row<25; row++)
    {
        for(col=0; col < 40; col++){
            cputcxy(col,row,map(level,col,row));
        }
    };

}

int main() {
    /* Clear Screen */
    clrscr();
    POKE(59468,12);
    //draw_screen();

    for(x=0;x<40;y+=4) {
    for(y=0;y<20;y++) {
        itoa (i,buffer,DECIMAL);
        cputsxy(x,y,buffer);
        cputcxy(x+2,y,i);
        i++;
    }}

    /* Hide cursor */
    cursor(0);
    printf("map 1");
    cputcxy(x,y,'@');

    /* Loop until Q is pressed */
    while ((key  = cgetc()) != 'Q')
    {


        // Delete the character
        cputcxy(x,y,map(level,x,y));

        // Backup the location
        old_x = x;
        old_y = y;

        // keys;
        switch (key) 
        { 
            case 'w':
                if(y>0) y--; 
                break; 
            case 'a': 
                if(x>0) x--; 
                break; 
            case 's': 
                if(y<24) y++; 
                break; 
            case 'd': 
                if(x<39) x++; 
                break; 
            default: 
                break; 
        }


        // Draw new location
        cputcxy(x,y,64);
     
    }
    cursor(1);
    return(0);
}
