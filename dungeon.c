void game_loop() {

    gotoxy(0,24);
    printf(" k: %02d S: %03d *: %03d score: %04d", keys,health, magic, score);

    // Change direction
    if(x != old_x || y != old_y) {
        direction_x = x-old_x;
        direction_y = y-old_y;
    }


    move_enemies();

    // Backup the location
    old_x = x;
    old_y = y;

    // keys;
    switch (key=cgetc()) 
    { 

        case 'w':
            if(y>0) y--; 
            break; 
        case 'a': 
            if(x>0) x--; 
            break; 
        case 'A': 
            if(sword==true) {
                draw_momentary_object(x-1,y,x-1,y,131,2000); 
                attack(10,x-1,y);
            }
            break;     
        case 's': 
            if(y<24) y++; 
            break; 
        case 'd': 
            if(x<39) x++; 
            break; 
        case 'D': 
            if(sword==true) {
                draw_momentary_object(x+1,y,x+1,y,31,2000); 
                attack(10,x+1,y);
            }
            break; 
        case 'f': 

            if(magic > 5) {

                magic-=5;
                fx = x+direction_x;
                fy = y+direction_y;  

                c=map(fx,fy);
                while(c==32 && magic > 0) {             
                    draw_momentary_object(fx,fy,fx,fy,'*',200); 
                    magic-=1;
                    fx = fx+direction_x;
                    fy = fy+direction_y;  
                    c=map(fx,fy);
                }

                attack(10,fx,fy);

              }

            break;            
        case 'Q':
            in_play = false;
            break;
        default: 
            break; 
    }

    // Anything in our path?
    obstruction=false;
    c=map(x,y);
    
    // Collision
    switch (c)
    {
        case 166:
            // Wall               
            obstruction=true;
            break;
        
        case 11: // Key +1
            keys+=1;
            break;

        case 9:
        case 45:
            if(keys>0)
            {
                keys-=1;
                score+=5;
                obstruction=false;

            }else{

                // Not enough keys to unlock!
                set_map(x, y, 28); // turn into partially open
                health-=10; // lose 10 health
                obstruction=true;
            }
            break;

        case 28: // Partially open door

            if(keys>0)
            {
                keys-=1;
                score+=5;
                obstruction=false;

            }else{
                // Not enough keys to unlock!
                set_map(x, y, 32);  // turn into fully open
                health-=10;         // lose 10 health
                obstruction=true;
            }
            break;

        case 30: // Sword!
            sword=true;
            break;

        case 36: // Cash money
            score+=5;
            break;

        case 145: // Potion
            score+=15;
            magic+=100;
            break;

        case 154: // Cash money
            score+=15;
            break;

        case 147: // Health
            health+=25;
            break;


/* Enemies >> */

        case 38: // Gobbo
            attack(5,x,y);
            obstruction=true;
            break;

        case 158: // Rats
            attack(5,x,y);
            obstruction=true;
            break;

/* ^^ Enemies */

        case 152: // Idol
            score+=10;
            idols+=1;
            if(idols==2) {
                room+=1;
                load_room();
                draw_screen();
                idols=0;
            }
            break;

        case 64: // Player
            break;
        
        default:
            
            // convert integer to ascii: itoa(c,buffer,10);
            
            if(c!=32) {
                gotoxy(0,0);
                printf("%03d",c);
                obstruction=true;
            }
            
            break;
    }

    // If obstructed then bounce
    if(obstruction) {
        x=old_x;
        y=old_y;
    } else {
        draw_move(false);
    }

    if(health<1) {
        in_play = false;
    }

    
    
}


int main() {

    // Use current time as 
    // seed for random generator 
    srand(1); 

    /* Clear Screen */
    clrscr();

#ifdef __PET__
    // Uppercase/Graphical characterset = 12
    POKE(59468,12);
#endif

#ifdef __C64__

    // Screen colors back to black + black border
    POKE(53281,0);
    POKE(53280,0);

    // Text color to match PET
    POKE(646,5);

    // Uppercase/Graphical characterset
    POKE(53272,21);

#endif

    /* Hide cursor */
    cursor(0);

    // Titles
    title_screen();

    // Start running     
    run=true;
        
    // Should the program end?
    while(run){

        // Initialize if not already running a game
        if (in_play != true) {
            keys=0;
            health=100;
            score=0;
            keys=0;
            room=0;
            potion=0;
            magic=0;
            enemy_count=0;
            sword=false;
        }

        // Set up the screen
        load_room();
        draw_screen();
        cputcxy(x,y,'@');

        // Game on!
        in_play = true;

        /* Loop until game ends */
        while (in_play)
        {
            game_loop();
        }

        // Try again or quit?
        run=game_over();

    }
    clrscr();
    printf("goodbye!\n\n");
    cursor(1);
    return(0);
}
